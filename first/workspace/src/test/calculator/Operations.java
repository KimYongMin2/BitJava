package test.calculator;

public class Operations {
    public float add(float a, float b){
        return a + b;
    }

    public float sub(float a, float b){
        return a - b;
    }

    public float mul(float a, float b){
        return a * b;
    }

    public float iMul(float a, float b){
        return a / b;
    }
}
